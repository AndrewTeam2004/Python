# Эта программа по лабораторной работе №1 24-СТвТ Ландяков А.
# Программа запрашивает у пользователя четырехзначное число,
# проверяет корректность ввода и вычисляет сумму и произведение его цифр.

while True:
    # Запрос числа у пользователя
    user_number = input("Введите четырехзначное число:")

    # Проверка на пустой ввод
    if user_number == "":
        print("Ошибка, введите число!")
        continue  # повтор запроса ввода числа

    # Попытка преобразовать строку в целое число
    try:
        input_number = int(user_number)
    except ValueError:
        print("Ошибка: введено не число!")
        continue 

    # Проверка, что число четырехзначное
    if len(user_number) != 4:
        print("Вы ввели не четырехзначное число!")
        continue  

    # Если все проверки пройдены — выходим из цикла
    break


# Выделение отдельных цифр числа с помощью арифметических операций
number1 = input_number // 1000          
number2 = (input_number // 100) % 10    
number3 = (input_number // 10) % 10     
number4 = input_number % 10             

# Вывод 
print("Введеное число:", input_number)

# Вычисление и вывод суммы цифр
print(
    "Сумма цифр:",
    number1, "+", number2, "+", number3, "+", number4, "=",
    (number1 + number2 + number3 + number4),
)

# Вычисление и вывод произведения цифр
print(
    "Произведение цифр:",
    number1, "*", number2, "*", number3, "*", number4, "=",
    (number1 * number2 * number3 * number4),
)